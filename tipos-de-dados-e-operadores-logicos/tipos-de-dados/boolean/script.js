/** Boolean */
console.log(typeof true);
console.log(typeof false);
console.log(typeof (10 > 2));
console.log(typeof (5 > 200));

/** Comparações com Booleans */
console.log(1 > 2);
console.log(5 < 10);
console.log(3 >= 3);
console.log(5 <= 4);

console.log(5 == 4);
console.log("Leonardo" != "Leonardo");
console.log(3 === "3");
console.log(4 === 4);
